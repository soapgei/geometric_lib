def area(a, b, c):
    '''Принимает три числа a, b, c - стороны треугольника, возвращает их сумму,
     деленную на 2 - площадь фигуры.'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Принимает три числа a, b, c - стороны треугольника,
    возвращает их сумму - периметр фигуры'''
    return a + b + c
